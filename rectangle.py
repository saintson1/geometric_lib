# area function
#  area counting of rectangle
#   args:
#       int a : widths
#       int b : height
#   return:
#       int : area of rectangle with lenths a;b
def area(a, b): 
    return a * b 

# perimeter function
#  perimeter counting of rectangle
#   args:
#       int a : widths
#       int b : height
#   return:
#       int : perimeter of rectangle with lenths a;b
def perimeter(a, b): 
    return (a + b) * 2