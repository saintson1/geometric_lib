import math

# area function
#  area counting by radius
#   args:
#       int r : circle radius
#   return:
#       int : area of circle with radius r
def area(r):
    return math.pi * r * r

# perimeter function
#  perimeter counting by radius
#   args:
#       int r : circle radius
#   return:
#       int : perimeter of circle with radius r
def perimeter(r):
    return 2 * math.pi * r

