# area function
#  area counting of triangle
#   args:S
#       int a : widths of base
#       int h : height
#   return:
#       int : area of triangle with lenths a
def area(a, h): 
    return a * h / 2 

# perimeter function
#  perimeter counting of triangle
#   args:
#       int a : side of base
#       int b : side of base
#       int c : side of base
#   return:
#       int : perimeter of triangle with sides a;b;c
def perimeter(a, b, c): 
    return a + b + c 